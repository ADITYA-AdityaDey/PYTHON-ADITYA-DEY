number1 = int(input("PLEASE ENTER THE FIRST NUMBER  = "))
number2 = int(input("PLEASE ENTER THE SECOND NUMBER = "))


sumOfTwo = number1 + number2

avgOfTwo = sumOfTwo / 2



print('THE SUM OF {0} AND {1}       = {2}'.format(number1, number2, sumOfTwo))
print('THE AVERAGE OF {0} AND {1}   = {2}'.format(number1, number2, avgOfTwo))
